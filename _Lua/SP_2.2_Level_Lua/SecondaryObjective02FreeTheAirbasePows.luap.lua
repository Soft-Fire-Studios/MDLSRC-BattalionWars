      pow02 = 1
      SetObjectiveData(Objective.FreePowsInTheAirbase, constant.OBJECTIVE_DATA_STATE, 1)
      SetObjectiveData(Objective_Marker.FreePowsInTheAirbase, constant.OBJECTIVE_MARKER_DATA_VISIBLE, 0)
      PhoneMessageWithObjective(55, constant.ID_NONE, constant.ARMY_WF, 0, SpriteID.CO_WF_Young_Herman_Happy, constant.PLAYER_ONE, false, constant.SECONDARY_OBJECTIVE_COMPLETE)
