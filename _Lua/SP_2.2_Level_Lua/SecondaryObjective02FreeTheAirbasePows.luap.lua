        CameraFade(constant.FADE_IN, constant.WAIT, 1)
        WaitFor(0.5)
        UnfreezePlayer(constant.PLAYER_ONE)
        WaitFor(1)
      else
        EndFrame()
      end
    end
    WaitFor(0.2)
  until Objective02 == 1
