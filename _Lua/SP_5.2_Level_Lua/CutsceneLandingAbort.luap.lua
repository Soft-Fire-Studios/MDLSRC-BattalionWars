  AddToGroup(Unit_Group.LandUnits, Troop.TAirTransport1Pilot4)
  AddToGroup(Unit_Group.LandUnits, Troop.TAirTransport1Pilot5)
  AddToGroup(Unit_Group.LandUnits, Troop.TAirTransport1Pilot6)
  AddToGroup(Unit_Group.LandUnits, Troop.TAirTransport2Pilot2)
  AddToGroup(Unit_Group.LandUnits, Troop.TAirTransport2Pilot3)
  AddToGroup(Unit_Group.LandUnits, Troop.TAirTransport2Pilot4)
  AddToGroup(Unit_Group.Player_Army, Ground_Vehicle.TLightTank1)
  AddToGroup(Unit_Group.Player_Army, Troop.TAirTransport1Pilot2)
  AddToGroup(Unit_Group.Player_Army, Troop.TAirTransport1Pilot3)
  AddToGroup(Unit_Group.Player_Army, Troop.TAirTransport1Pilot4)
  AddToGroup(Unit_Group.Player_Army, Troop.TAirTransport1Pilot5)
  AddToGroup(Unit_Group.Player_Army, Troop.TAirTransport1Pilot6)
