  beachlandingcomplete = 1
  GoToArea(Air_Vehicle.TAirTransport1, 623, -179, 5, 0, constant.ORDER_FORCED)
  GoToArea(Air_Vehicle.TAirTransport2, 625, -181, 5, 0, constant.ORDER_FORCED)
