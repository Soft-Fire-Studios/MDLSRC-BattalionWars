function Activate_Base(owner)
  WaitFor(5)
  repeat
    EndFrame()
  until IsInArea(GetPlayerUnit(), Map_Zone.Base_Zone)
  SetObjectiveData(Objective.GetToBase, constant.OBJECTIVE_DATA_STATE, 1)
  SetActive(Troop.WF_Bazooka1, constant.ACTIVE)
  SetActive(Troop.WF_Bazooka2, constant.ACTIVE)
  SetActive(Troop.WF_Bazooka3, constant.ACTIVE)
