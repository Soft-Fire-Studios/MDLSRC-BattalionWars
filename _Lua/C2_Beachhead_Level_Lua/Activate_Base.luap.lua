  SetActive(Troop.WF_Chain2, constant.ACTIVE)
  SetActive(Troop.WF_Chain3, constant.ACTIVE)
  SetActive(Troop.WF_Rifle1, constant.ACTIVE)
  SetActive(Troop.WF_Rifle2, constant.ACTIVE)
  SetActive(Troop.WF_Rifle3, constant.ACTIVE)
  SetActive(Troop.WF_Rifle4, constant.ACTIVE)
  SetActive(Troop.WF_Rifle5, constant.ACTIVE)
  SetActive(Troop.WF_Rifle6, constant.ACTIVE)
